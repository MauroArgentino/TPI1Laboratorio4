            $datos = [
                "username" => trim($_POST["username"]),
                "nombre" => trim($_POST["nombre"]),
                "apellido" => trim($_POST["apellido"]),
                "email" => trim($_POST["email"]),
                "password" => trim($_POST["password"]),
            ];
            if ($this->userModel->createUser($datos)){
                header("location: ".URL_PATH);
            }
        }
    }

